function switchItUp(number){
  let result = "Zero";
  switch(number){
    case 1:
      result = "One";
    case 2:
      result = "Two";
    case 3:
      result = "Three";
    case 4:
      result = "Four";
    case 5:
      result = "Five";
    case 6:
      result = "Six";
    case 7:
      result = "Seven";
    case 8:
      result = "Eight";
    case 9:
      result = "Nine";
  }
  return result;
}
