//square
Array.prototype.square = function () {
    var result = [];
    for (var i = 0; i < this.length; i++) {
        result.push(this[i]**2);
    }
    return result;
};

//cube
Array.prototype.cube = function () {
    var result = [];
    for (var i = 0; i < this.length; i++) {
        result.push(this[i]**3);
    }
    return result;
};


